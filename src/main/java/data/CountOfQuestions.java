package data;

/**
 * Class for the total number of questions
 * @author Samantha
 *
 */
public class CountOfQuestions {
	private int qcoun;
	
	public CountOfQuestions() {
		
	}
	public CountOfQuestions(int qcoun) {
		this.qcoun = qcoun;
	}

	public int getQcoun() {
		return qcoun;
	}

	public void setQcoun(int qcoun) {
		this.qcoun = qcoun;
	}

}
