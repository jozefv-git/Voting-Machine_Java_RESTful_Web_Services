package data;

/**
 * 
 * Class used for the total number of candidates
 * @author Cezar
 *
 */
public class CountOfCand {
	private int nr;
	
	public CountOfCand() {
		
	}
	
	public CountOfCand(int nr) {
		this.nr = nr;
	}

	public int getNr() {
		return nr;
	}

	public void setNr(int nr) {
		this.nr = nr;
	}
}
